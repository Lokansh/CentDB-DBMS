public class database {
    private int dbId;
    private String dbName;
    private String dbCreatedDate;
    private String dbCreatedBy;
    private String dbStatus;

    public int getDbId() {
        return dbId;
    }

    public void setDbId(int dbId) {
        this.dbId = dbId;
    }

    public String getDbName() {
        return dbName;
    }

    public void setDbName(String dbName) {
        this.dbName = dbName;
    }

    public String getDbCreatedDate() {
        return dbCreatedDate;
    }

    public void setDbCreatedDate(String dbCreatedDate) {
        this.dbCreatedDate = dbCreatedDate;
    }

    public String getDbCreatedBy() {
        return dbCreatedBy;
    }

    public void setDbCreatedBy(String dbCreatedBy) {
        this.dbCreatedBy = dbCreatedBy;
    }

    public String getDbStatus() {
        return dbStatus;
    }

    public void setDbStatus(String dbStatus) {
        this.dbStatus = dbStatus;
    }
}
